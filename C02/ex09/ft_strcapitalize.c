char	*ft_strcapitalize(char *str)
{
	int flag;
	int i;

	flag = 1;
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			if (flag == 1)
				str[i] = str[i] - 32;
			flag = 0;
		}
		else if (str[i] >= 48 && str[i] <= 57)
			flag = 0;
		else
			flag = 1;
		i++;
	}
	return (str);
}
