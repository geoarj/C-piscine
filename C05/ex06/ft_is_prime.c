int		ft_is_prime(int nb)
{
	int i;

	i = nb - 1;
	if (nb <= 1)
		return (0);
	else
	{
		while (nb % i == 0)
		{
			i--;
		}
		if (i > 1)
			return (0);
	}
	return (1);
}
